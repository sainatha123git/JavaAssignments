package com.jalaacademy;

public class Logicalandornotoperators {
	public static void main(String[] args) {
		boolean a = true;
		boolean b = false;

		System.out.println("Logical operator :" + (a && b));
		System.out.println("OR operator :" + (a || b));
		System.out.println("not equal :" + (!a));
		System.out.println("not equal :" + (!b));

	}

}